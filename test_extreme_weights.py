# 極端な重みでのテスト

# ケース1: tk=98%, pc=98%
tk1 = 0.98
pc1 = 0.98
product1 = (tk1**320.0) * (pc1**160.0) * (0.9**8.0) * (0.9**4.0) * (0.8**2.0) * (0.7**1.0)
core1 = (product1 ** (1.0 / 495.0)) * 100.0

# ケース2: tk=97%, pc=97% (1%劣化)
tk2 = 0.97
pc2 = 0.97
product2 = (tk2**320.0) * (pc2**160.0) * (0.9**8.0) * (0.9**4.0) * (0.8**2.0) * (0.7**1.0)
core2 = (product2 ** (1.0 / 495.0)) * 100.0

print(f"ケース1 (tk=98%, pc=98%): core={core1:.4f}")
print(f"ケース2 (tk=97%, pc=97%): core={core2:.4f}")
print(f"差: {core1 - core2:.4f}")
print(f"劣化率: {((core1 - core2) / core1 * 100):.2f}%")
